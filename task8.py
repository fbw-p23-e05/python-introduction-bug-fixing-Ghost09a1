x = float(input("First number: "))  # Convert float
y = float(input("Second number: "))  # Convert float
z = float(input("Third number: "))  # Convert float

maximum_value = max(x, y, z)  # Use max() find maximum value
minimum_value = min(x, y, z)  # Use min() find minimum value

print("The maximum value is", maximum_value)
print("The minimum value is", minimum_value)
